/**
 * The class `ext_calculator` contains methods to calculate the square and cube of a given float
 * number.
 */
public class ext_calculator {
    /**
     * The function "square" takes a float input and returns the square of that input as a float.
     * 
     * @param x The parameter `x` in the `square` function represents the number that you want to
     * square.
     * @return The function `square` is returning the square of the input parameter `x`.
     */
    static float square (float x){
        return (x*x);
    }
    /**
     * The `cube` function in Java calculates the cube of a given float number.
     * 
     * @param x The parameter `x` in the `cube` function represents the number that you want to
     * calculate the cube of.
     * @return The function `cube` is returning the cube of the input parameter `x`. It calculates
     * `x*x*x` and returns the result as a float.
     */
    static float cube (float x){
        return (x*x*x);
    }
}
